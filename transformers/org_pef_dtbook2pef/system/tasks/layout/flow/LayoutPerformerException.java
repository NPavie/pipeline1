package org_pef_dtbook2pef.system.tasks.layout.flow;

/**
 * {@inheritDoc}
 */
public class LayoutPerformerException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2908554164728732775L;

	/**
	 * {@inheritDoc}
	 */
	public LayoutPerformerException() { }

	/**
	 * {@inheritDoc}
	 */
	public LayoutPerformerException(String message) {
		super(message);
	}

	/**
	 * {@inheritDoc}
	 */
	public LayoutPerformerException(Throwable cause) {
		super(cause);
	}

	/**
	 * {@inheritDoc}
	 */
	public LayoutPerformerException(String message, Throwable cause) {
		super(message, cause);
	}

}
