package org_pef_dtbook2pef.system.tasks.layout.flow;

public class Marker {
	private String name;
	private String value;
	
	public Marker(String name, String value) {
		this.name = name;
		this.value = value;
	}
	
	public String getName() {
		return name;
	}
	
	public String getValue() {
		return value;
	}

}
